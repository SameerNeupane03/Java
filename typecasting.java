public class typecasting {
    public static void main(String args[]){
    byte evenNum = 80;
    int typecastEvenNum = (int) evenNum;

    float floatingPoint = 59.88f;
    int var = (int) floatingPoint;

    char alphabet = 'a';
    float alphabetValue = (float) alphabet;

    System.out.println(typecastEvenNum);
    System.out.println(var);
    System.out.println(alphabetValue);

}}
