public class Literals {
    public static void main(String[] args){
        //Any value which is stored in a variable is a literal;

        //stores the binary value;
        int binaryValue = 0b1001;

        //stores the hexadecimal value;
        int hexValue = 0x883E;

        System.out.println("Binary Value: " + binaryValue + "\nHexadecimal Value: " + hexValue);

        float exponentialNumber = 100e10f;

        System.out.println("\nExponential Number: " + exponentialNumber);
    }
    
}
